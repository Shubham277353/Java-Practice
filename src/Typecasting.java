public class Typecasting {
    public static void main(String[] args) {
        int num = (int)(2004.2321f);
        // this is called type casting converting the numbers from heigher type to lower
        System.out.println(num);

        // AUTOMATIC casting from lower to hwigher in java
        float num1 = 2341;
        System.out.println(num1);
    }
}
